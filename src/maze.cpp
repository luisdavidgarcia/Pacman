void Maze::draw(int tileSize) const {
  for (int row = 0; row < ROWS; ++row) {
    for (int col = 0; col < COLS; ++col) {
      if (grid[row][col] == 1) {
        DrawRectangle(
            col * tileSize,
            row * tileSize,
            tileSize,
            tileSize,
            BLUE
        );
      }
    }
  }
}

